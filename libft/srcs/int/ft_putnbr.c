#include "../../includes/libft.h"

void	ft_putnbr(long int n)
{
	char c;

	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	c = n % 10 + '0';
	if (n >= 10)
		ft_putnbr(n / 10);
	write(1, &c, 1);
}
